# comparission operators 
x=10
y=3
print(x==y)
print(x!=y)
print(x>y)
print(x>=y)
print(x<y)
print(x<=y)
#logical operators
a=1
b=2
print(a and b)
print(a or b)
print(not b)
#bitwise
print(a & b)
print(a | b)
print(a ^ b)
print(~b)
print(a << b)
print(a >> b)
